    <h2></h2>
    <p>
        Uma máquina mais robusta, rápida e precisa, desenvolvida para executar tare-fas mais
        complexas em série ou até mesmos uma única peça. Seu tamanho e sua robustez a
        transformam na máquina ideal para a usinagem de peças com materiais variados de até 200
        mm de espessura. Com uma interface amigável fica fácil execu-tar as mais diversas
        tarefas, como importar arquivos de código G para trabalhos mais elaborados, ou operação
        em manual para trabalhos simples.
    </p>
